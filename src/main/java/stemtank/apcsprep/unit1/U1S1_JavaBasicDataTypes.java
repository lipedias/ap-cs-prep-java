package main.java.stemtank.apcsprep.unit1;

public class U1S1_JavaBasicDataTypes {

public static void main(String [] args){

        short JamesBondShort = 007;
        int JamesBondInteger  = 007;
        long JamesBondLong = 007L;
        float JamesBondFloat = 007;
        double JamesBondDouble = 007;
        char JamesBondChar = '7';
        boolean isJamesBondCool = true;
        // BONUS
        String JamesBondString = "007";

        // should we present byte?

        System.out.println("short -> " + JamesBondShort);
        System.out.println("int -> " + JamesBondInteger);
        System.out.println("long -> " + JamesBondLong);
        System.out.println("float -> " + JamesBondFloat);
        System.out.println("double -> " + JamesBondDouble);
        System.out.println("char -> " + JamesBondChar);
        System.out.println("boolean -> " + isJamesBondCool);
        System.out.println("String -> " + JamesBondString);
 }
}
